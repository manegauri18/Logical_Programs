package array;

import java.util.Arrays;

public class ConvertStringToBytes 
{
	public static void main(String[] args) 
	{
		String str ="Suresh";
		
		byte[] bytes = str.getBytes();
		
		System.out.println(Arrays.toString(bytes));
		
		System.out.println("------------------------------------------------");
		
		
		
	}

}
